// Comparison operator 
console.log(10 < 5 );
console.log(10 > 5 );
console.log(10 <= 5 );
console.log(10 >= 5 );

// (==) Loose Equality 
console.log(10 == "10" );
console.log(0 == false );

// (===) Strict Equality 
console.log(10 === "10");
console.log(10 === 10);
console.log(0 === false);

// != (Loose Inequality) & !== (Strict Inequality): The same rules apply. Always prefer !==. 
console.log("use different way like loose inequality and strict inequality");
console.log(10 != "10");
console.log(0 !== "false");